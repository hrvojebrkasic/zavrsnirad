            <div class="col-md-4">
                <p>Od 1991. godine kreće novo razdoblje kluba. U samostalnoj Hrvatskoj, klub se aktivira kroz niz selekcija s kojima igra prvenstva u organizaciji Hrvatskog
                    košarkaškog saveza i Košarkaškog saveza Zagreba. Od 1992. do 1998. godine seniori kluba se natječu u A-2 (2.liga) Hrvatskoj košarkaškoj ligi i tu ostaju sve do 1998.
                    Tada, zbog nedostatka sredstava, odustaju od igranja u 2. ligi te sve do 2002. godine igraju B-1 (3.ligu). 2002. godine vraćaju se u 2.
                    Hrvatsku košarkašku ligu gdje igraju punih 6 sezona. Kao uspješan drugoligaš, bez trzavica unutar kluba, osiguravaju svake godine opstanak u ligi (istina na dosta težak način),
                    ali isključivo sa igračima iz Siska i iz svoga podmlatka, regrutirajući igrače iz juniorskog pogona. Mislimo da smo jedini klub koji igra seniorsku ligu sa 100% svojih igrača.
                    Tek u sezoni 2007./08. po prvi put smo doveli igrača na centarskoj poziciji iz Zagreba.</p>
            </div>
            <div class="col-md-4">
                <p>Do 1997.godine u klubu se igrala samo muška košarka, dok je 1997. formirana i ženska ekipa te svake godine sa tri ekipe sudjelujemo u natjecanju
                    mlađih dobnih kategorija u ženskoj konkurenciji. U sezonama 2002./03. i 2003./04. djevojke su igrale seniorsku A-2 ligu,
                    no nakon te sezone odustalo se od ženske seniorske košarke zbog nedostatka sredstava, te se nastavilo igrati samo mlađim dobnim ženskim kategorijama.
                    Od 1996. godine klub je 13 puta za redom bio organizator klupskog košarkaškog kampa u Savudriji, mjestu kraj Umaga, gdje je u prosjeku godišnje kamp pohađalo 60-ak polaznika.
                    2006. i 2007. juniorske ekipe sudjeluju na međunarodnom juniorskom turniru u Francuskoj koji smo 2006.godine i osvojili.</p>
