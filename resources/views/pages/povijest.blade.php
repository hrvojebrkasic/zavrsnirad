                <p>Košarkaški klub “SISAK” u obitelji sisačkog sporta je od 1947. godine kada je u Sisku odigrana prva košarkaška utakmica.
                    Nakon II.svj.rata djelovale su u Gradu razne košarkaške sekcije i odigravale utakmice, a 5.srpnja 1968. godine održana je OSNIVAČKA SKUPŠTINA K.K.“SISAK“.
                    Nikada, do sada, klub nije bio pred gašenjem i odustajanjem od rada, natjecanja ni utakmica. Mijenjali su se igrači, treneri i uprave ali klub je oduvijek živio na ovim prostorima.
                    Čak i u doba bivše države, od 1970. do 1974. klub je nosio ime generalnog sponzora – „CROATIA“ (osiguravajuće kuće).</p>
